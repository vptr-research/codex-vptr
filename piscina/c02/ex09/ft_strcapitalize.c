char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	if (str[i] >= 'a' && str[i] <= 'z')
	{
		str[i] = str[i] - 32;
		i++;
	}
	else
		i++;
	while (str[i])
	{
		if (str[i] >= 'a' && (str[i] <= 'z'))
			if (((str[i - 1] >= 32 && str[i - 1] <= 47) || (str[i - 1] >= 58
						&& str[i - 1] <= 64)
					|| (str[i - 1] >= 123 && str[i - 1] <= 126)))
				str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
